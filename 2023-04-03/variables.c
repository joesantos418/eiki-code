#include <stdio.h>

int main() {
    /* declarando um int*/
    int a;

    /* atribuindo um valor */
    a = 10;
    printf("%d\n", a);

    /*
        atribuição tem uma direção
        10 = a;

        igualdade é uma operação de comparação com ==
    */

    /*posso declarar e atribuir*/
    int b = 30;
    printf("%d\n", b);

    float c = 100.5;
    printf("%f\n", c);

    double d = 10.4;
    printf("%lf\n", d);

    char s = 'c';
    printf("%c\n", s);

    return 0;
}